void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)//串口3接收完成回调函数
{
  static uint8_t complete_flag = 1;
  static uint8_t RxFlag = 0;
  static uint8_t Rx1Flag = 0;

  // imu相关变量
  static uint8_t Count=0;
	static uint8_t rs_count=0;
  static uint8_t last_rsnum=0;
	uint8_t Usart_Receive;
	static uint8_t rsimu_flag=0;
	static uint8_t rsacc_flag=0;
  static GEOData_Packet_t msg;
  

  if(huart->Instance == USART1) {
    // flag = 2;
    // osMessageQueuePut(gpshuart_flagqHandle, &flag, 2U, 0U); //
    if (Rx1Temp[0] == '2') delete_flag = 1;
    if (Rx1Temp[0] == '3') {
      Rx1Flag = 2;
      osMessageQueuePut(gpshuart_flagqHandle, &Rx1Flag, 2U, 0U); //将接收到的数据发送到队列
    }     
    HAL_UART_Receive_IT(&huart1,(uint8_t *)Rx1Temp, REC_LENGTH);	//重新使能中断 
    
    
  }

  if(huart->Instance == UART5) {
    // printf("uart5: get\r\n");
    Fd_data[Count] = Rx5Temp[0];			  // 串口数据填入数组
    // printf(" %x", Rx5Temp[0]);

    Usart_Receive = Rx5Temp[0];
		if (((last_rsnum == FRAME_END) && (Usart_Receive == FRAME_HEAD)) || Count > 0)
		{
      // printf("uart5: get frame\r\n");
      

			rs_count = 1;
			Count++;
      // 经纬度数据
      if ((Fd_data[1] == TYPE_GEODETIC_POS)&& (Fd_data[2] == GEODETIC_POS_LEN)){//0x20
				rsgeo_flag = 1;
        // printf("\r\n geo \r\n"); 
      }
		}
		else
			Count = 0;
		last_rsnum = Usart_Receive;


		
    if (rsgeo_flag == 1 && Count == GEODETIC_POS_RS) //
		{
      printf("%x, %x \r\n", Fd_data[1], Fd_data[2]);
      Count = 0;
      rsgeo_flag = 0;
      rs_geotype = 1;
      if (Fd_data[GEODETIC_POS_RS - 1] == FRAME_END) {
        printf("correct\r\n");
        memcpy(Fd_rsgeo, Fd_data, sizeof(Fd_data));
        TTL_Hex2Dec();
        // osMessageQueuePut(imu_msgHandle, &msg, 2U, 0U); //将接收到的数据发送到队列
        // print_imu_data(&msg);
      } else {
        printf(" %x \r\n", Fd_data[GEODETIC_POS_RS - 1]);
      }
      for (int i = 0; i < sizeof(Fd_data); i++)
        Fd_data[i] = 0;
    }
    // HAL_UART_Transmit(&huart1, (uint8_t *)Rx5Temp, REC_LENGTH, 100);
    // SET_BIT(UART4->RQR, USART_RQR_RXFRQ);
    HAL_UART_Receive_IT(&huart5,(uint8_t *) Rx5Temp, REC_LENGTH);	//重新使能中断
  }


	if(huart->Instance == USART2) {
    // HAL_UART_Transmit(&huart1, (uint8_t *)RxTemp, REC_LENGTH, 100);
    if (RxTemp[0] == '$' || RxTemp[0] == '#') { //如果接收到数据头
      // printf("head received \r\n");
      if (complete_flag == 0) { //如果是首次数据头
        RxCounter = 0; //计数器清零
        complete_flag = 1;
      } else { //如果已经接收到过一次数据头且数据没结束
        complete_flag = 0; //重新开始接收
      }
    } 

    if (complete_flag == 1)
		{
      RxBuffer[RxCounter] = RxTemp[0];							//缓存数据放入接收数组
      RxCounter++;												//计数器加1
    }
    else {
      RxCounter = 0;
    }

    if (RxTemp[0] == '*' && complete_flag == 1) { //如果接收到数据尾
      // printf("end received \r\n");
      complete_flag = 0; //重新开始接收
      RxBuffer[RxCounter] = '\0'; //添加字符串结束符
      RxCounter = 0; //计数器清零
      RxFlag = 1;
      osMessageQueuePut(gpshuart_flagqHandle, &RxFlag, 2U, 0U); //将接收到的数据发送到队列

    }

    HAL_UART_Receive_IT(&huart2,(uint8_t *)RxTemp, REC_LENGTH);	//重新使能中断
	}


}

