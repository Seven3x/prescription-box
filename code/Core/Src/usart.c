void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart)//串口3接收完成回调函数
{
  static uint8_t complete_flag = 1;
  static uint8_t RxFlag = 0;
  static uint8_t Rx1Flag = 0;

  

  if(huart->Instance == USART1) {
    // flag = 2;
    // osMessageQueuePut(gpshuart_flagqHandle, &flag, 2U, 0U); //
    if (Rx1Temp[0] == '2') delete_flag = 1;
    if (Rx1Temp[0] == '3') {
      Rx1Flag = 2;
      osMessageQueuePut(gpshuart_flagqHandle, &Rx1Flag, 2U, 0U); //将接收到的数据发送到队列
    }     
    HAL_UART_Receive_IT(&huart1,(uint8_t *)Rx1Temp, REC_LENGTH);	//重新使能中断 
    
    
  }



	if(huart->Instance == USART2) {
    // HAL_UART_Transmit(&huart1, (uint8_t *)RxTemp, REC_LENGTH, 100);
    if (RxTemp[0] == '$' || RxTemp[0] == '#') { //如果接收到数据头
      // printf("head received \r\n");
      if (complete_flag == 0) { //如果是首次数据头
        RxCounter = 0; //计数器清零
        complete_flag = 1;
      } else { //如果已经接收到过一次数据头且数据没结束
        complete_flag = 0; //重新开始接收
      }
    } 

    if (complete_flag == 1)
		{
      RxBuffer[RxCounter] = RxTemp[0];							//缓存数据放入接收数组
      RxCounter++;												//计数器加1
    }
    else {
      RxCounter = 0;
    }

    if (RxTemp[0] == '*' && complete_flag == 1) { //如果接收到数据尾
      // printf("end received \r\n");
      complete_flag = 0; //重新开始接收
      RxBuffer[RxCounter] = '\0'; //添加字符串结束符
      RxCounter = 0; //计数器清零
      RxFlag = 1;
      osMessageQueuePut(gpshuart_flagqHandle, &RxFlag, 2U, 0U); //将接收到的数据发送到队列

    }

    HAL_UART_Receive_IT(&huart2,(uint8_t *)RxTemp, REC_LENGTH);	//重新使能中断
	}

  if(huart->Instance == UART4) {
    HAL_UART_Transmit(&huart1, (uint8_t *)Rx4Temp, REC_LENGTH, 100);
    HAL_UART_Receive_IT(&huart4,(uint8_t *)Rx4Temp, REC_LENGTH);	//重新使能中断
  }

}

